//Three number which is largest number.
class ThreeNumber{
    public static void main(String[] args){
        int a, b, c;
        a = 10; b = 9; c = 8;
        if(a > b && a > c){
            System.out.println(a + " is largest number");
        }
        else if(b > a && b > c){
            System.out.println(b + " is largest number");
        }
        else{
            System.out.println(c + " is largest number");
        }
    }
}



