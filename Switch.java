//switch statement
class Switch{
	public static void main(String[] args){
		int num = 10;
		switch(num){
			case 5:
			{
				System.out.println("This is 5");
				break;
			}
			case 10:
			{
				System.out.println("This is 10");
				break;
			}
			default:
			{
				System.out.println("This is out of range");
				break;
			}
		}
	}
}

